package br.com.alura.forum.modelo;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import javax.persistence.*;
import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

//Classe de domínio nada mais é do que uma representação de uma entidade de negócio

//Devemos avisar para o Spring Security qual classe representa o usuário no sistema através da interface UserDetails

@Entity
public class Usuario implements UserDetails {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String nome;
    private String email;
    private String senha;

    // Para o Spring Security além de ter uma classe usuário temos que ter uma classe que representa o perfil daquele usuário
    // Como a classe perfil é uma entidade precisamos informar a cardinalidade do relacionamento de usuário para perfil
    @ManyToMany(fetch = FetchType.EAGER)
    // Como um usuario pode ter varios perfis e um perfil pode estar atrelado a mais de um usuario ao mesmo tempo inserimos @ManyToMany
    // Por padrão na JPA, relacionamentos ToMany se carregarmos do BD o usuario, ele não carrega essa lista porque é lazy, por isso foi inserido o fetch com o enum EAGER
    private List<Perfil> perfis = new ArrayList<Perfil>();

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Usuario other = (Usuario) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return this.perfis;
    }

    @Override
    public String getPassword() {
        return this.senha;
    }

    @Override
    public String getUsername() {
        return this.email;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
